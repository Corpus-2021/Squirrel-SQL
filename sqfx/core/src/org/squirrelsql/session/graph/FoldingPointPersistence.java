package org.squirrelsql.session.graph;

public class FoldingPointPersistence
{
   private double _x;
   private double _y;

   public void setX(double x)
   {
      _x = x;
   }

   public double getX()
   {
      return _x;
   }

   public void setY(double y)
   {
      _y = y;
   }

   public double getY()
   {
      return _y;
   }
}
